// Locales that share English translations (fallbacks)
const FALLBACK_TO_EN = ['en-US'];

// ── Inject into each locale file ────────────────────────────────────────────
const files = fs.readdirSync(LOCALES_DIR).filter((f) => f.endsWith('.json'));
let updated = 0;
let skipped  = 0;

for (const file of files) {
  const locale = file.replace('.json', '');
  const filePath = path.join(LOCALES_DIR, file);

  let raw;
  try { raw = fs.readFileSync(filePath, 'utf8'); } catch { console.warn(`  skip (unreadable): ${file}`); continue; }

  let json;
  try { json = JSON.parse(raw); } catch { console.warn(`  skip (invalid JSON): ${file}`); continue; }

  if (!json.careers) { console.warn(`  skip (no careers section): ${file}`); skipped++; continue; }

  // Already has new keys? skip
  if (json.careers.applyPageTitle) { console.log(`  already done: ${file}`); skipped++; continue; }

  const trans = T[locale] ?? T[FALLBACK_TO_EN.includes(locale) ? 'en' : 'en'];
  if (!trans) { console.warn(`  no translation found for: ${locale}, using en`); }
  const t = trans ?? T['en'];

  // Merge new keys into careers section
  json.careers = { ...json.careers, ...t };

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2) + '\n', 'utf8');
  console.log(`  updated: ${file}`);
  updated++;
}

console.log(`\nDone. ${updated} files updated, ${skipped} skipped.`);
